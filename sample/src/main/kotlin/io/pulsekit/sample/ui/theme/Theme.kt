/**
 * PulseKit Material 3 theme for the sample app.
 *
 * @param darkTheme follow the system setting by default.
 * @param dynamicColor use Material You wallpaper-based colors on Android 12+
 *   (like the Now in Android sample); falls back to the brand palette below.
 */
@Composable
fun PulseKitTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit,
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColors
        else -> LightColors
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = PulseTypography,
        content = content,
    )
}
